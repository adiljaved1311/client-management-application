    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String s1 = u.getText();
            String s2 = String.valueOf(p.getPassword());
            ResultSet rs=db.DbConnect.st.executeQuery(
              "select * from admin_login Where aid='"+s1+"'and pass='"+s2+"'");
            if(rs.next()){
                String n=rs.getString(3);
                //String n= rs.getString("name");  //allowed
                new Home(n).setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null,"Wrong Entries!");
            }
        }
        catch(Exception e){
            JOptionPane.showConfirmDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

